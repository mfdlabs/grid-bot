namespace Grid.Bot.Utility;

/// <summary>
/// The type of a setting.
/// </summary>
public enum SettingType
{
    /// <summary>
    /// String type setting -- default.
    /// </summary>
    String,

    /// <summary>
    /// Boolean type setting -- a flag.
    /// </summary>
    Bool,

    /// <summary>
    /// Integer type setting.
    /// </summary>
    Int
}
